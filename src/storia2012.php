Il 2012 fu un anno molto importante per la Festa di Scienze. All'inizio, in fase di organizzazione, si doveva tenere conto dei problemi degli anni scorsi. Poich&egrave; anche il 2011 non aveva fruttato moltissimo alla beneficenza, la paura che l'inefficacia risiedesse nell'assetto adottato fino ad allora si faceva sempre pi&ugrave; pressante. Il timore era quello d'esser riusciti a creare un evento che per&ograve;, negli anni, non avrebbe saputo essere autosufficiente. L'idea originaria di Wudy aveva tra i capisaldi quello di rimanere un evento indipendente, per il quale servissero solo tanta buona volont&agrave; ed entusiasmo. Una serata senza intoppi, che per&ograve; produceva poca beneficenza lasciando ben poco margine di errore. In realt&agrave; le speranze non erano del tutto crollate. Il 2011 aveva lasciato un bellissimo ricordo ma non si pu&ograve; dire fosse stata una festa "esente da intoppi", anzi. A met&agrave; serata lo stand delle birre si blocc&ograve; a seguito di un guasto all'impianto di spillatura. Guasto che provoc&ograve; non pochi disguidi. 
<br>Rimaneva quindi un ultimo fattore da "correggere" prima di poter valutare il vero potenziale dell'evento. Per ridurre al minimo la possibilit&agrave; di guasti, non solo vennero raddoppiati gli impianti di spillatura, ma vennero anche potenziati per evitare cedimenti nei momenti clu. Fu proprio da questo aspetto che emerse l'ispirazione per il nuovo motto: "Ho'oponopono", una parola Hawaiana che significa "mettere le cose al posto giusto", in altre parole "aggiustare le cose".
<br>
<br />
L'altro fattore, pi&ugrave; storico che tecnico, che contribu&igrave; a rendere quell'anno memorabile fu l'ultimo decreto Gelmini che segnava la fine delle facolt&agrave; e la nascita dei Dipartimenti. In quell'anno, pi&ugrave; che mai, serviva sviluppare un senso d'appartenenza ed un gioco di squadra che potesse persistere anche negli anni successivi, quando la cara ed amata Facolt&agrave; di Scienze MM.FF.NN si sarebbe divisa nei due dipartimenti: Biotecnologie e Informatica.
<br>
<img class="imgRight" src="../assets/img/storia/2012/gallery_fsu_2012_0337.jpg" alt=""/>
Consapevole della sua imminente laurea, Wudy (al secolo Walter Riviera) pens&ograve; che sarebbe stato opportuno iniziare a pensare ad un passaggio di testimone per quanto riguarda l'organizzazione dell'evento. Con l'obiettivo di permettere sempre a pi&ugrave; persone di poter dare il contributo alla festa, decise di istituire un secondo STAFF oltre a quello "esecutivo" che prestava servizio in serata: lo STAFF "organizzativo".
<br>
Per alleggerire il carico e massimizzare i risultati, lo STAFF "organizzativo" venne strutturato in 4 commissioni: la comm. grafica, la comm. forniture e servizi, la comm. ricerca sponsor e la comm. burocrazia. Ognuna di esse era formata da studenti volontari, trainati da altri studenti pi&ugrave; esperti con la nomina di responsabili.
<br>
<br />
Questa scelta port&ograve; alla creazione di un'altra attivit&agrave;: la scelta delle locandine! Avendo a disposizione una commissione dedicata (quella grafica), vennero prodotti pi&ugrave; prototipi di locandine, e ad ogni studente venne data la possibilit&agrave; di esprimere la propria preferenza attraverso un voto on-line tramite piattaforma E-Learning.
<br>
<br />Un'ulteriore iniziativa fu quella di introdurre una parte di "intrattenimento" durante la serata, appena prima dell'inizio del concerto. 
Si cre&ograve; cos&igrave; la SFILATA a m&ograve; di "ciao Darwin", con i vari scenari che ritraevano gli iscritti ai corsi erogati da Scienze MM.FF.NN, tutto ovviamente in chiave ironica. 
<br>
La serata fu alquanto indimenticabile! Malgrado qualche piccolo attimo di paura dovuto ad un "momentaneo" stand-by di uno dei due impianti di spillatura, tutto prosegu&igrave; liscio. 
Finalmente i risultati sperati arrivarono come una ventata d'aria fresca d'estate: erano riusciti a devolvere una quota "considerevole" in beneficienza, pur lasciando invariato l'assetto economico dei servizi forniti!
<br>
<br />
A conferma del successo a 360&deg;, il preside (per l'ultimo anno!) Prof. Roberto Giacobazzi, torn&ograve; accompagnato da un articolo sull'UniVr Magazine:
<p>
"La festa di fine anno della Facolt&agrave; di Scienze &egrave; diventato un appuntamento molto atteso dagli studenti, e non solo. La festa &egrave; interamente pensata e realizzata dagli studenti, e questo &egrave; forse l'aspetto pi&ugrave; bello di questa idea: per una notte sono gli studenti che gestiscono gli spazi dell'universit&agrave;. Non &egrave; un caso che i ricordi pi&ugrave; belli degli anni universitari siano proprio quelli legati alla condivisione degli spazi, per lo studio ma anche per il divertimento e per lo stare insieme. Tutto sommato ci vuole poco per fare un campus, basta uno spazio e la voglia di farlo vivere insieme".
</p><br>
