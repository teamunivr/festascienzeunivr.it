                        "legale" dell'evento. L'intenzione era infatti quella di organizzare la prima festa un anno
                        dopo, nel giugno 2010. Tuttavia, la ricerca avviata da Chiara in merito alle implicazioni
                        burocratiche, fu cos&igrave; veloce e precisa che diede ancora pi&ugrave; animo all'idea e inizi&ograve;
                        a far pensare di anticipare i tempi.
                        <br>
                        <br/>
                        Bench&egrave; la burocrazia pareva meno complicata del previsto, rimanevano da definire aspetti
