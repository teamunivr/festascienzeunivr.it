                        compagni di corso del rappresentante, si distinsero per l'interesse e la dedizione: l'amico
                        sardo Lino Faedda, l'amico veronese Paolo -Paolino- Erbizi.
                        <br>
                        <br/>
                        Anche se la bozza del progetto era molto umile, presentava gi&agrave; sei linee guida, chiare e
                        parecchio ambiziose:
                        <br>
                        * Prezzi contenuti, La festa avrebbe dovuto accogliere studenti con lo scopo di unire persone,
