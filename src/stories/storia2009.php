                        accademico (2008-2009) era stato segnato dal D.L. Gelmini che spinse molti studenti a
                        mobilitarsi a seguito della riforma universitaria. Tra questi anche Wudy, al secolo Walter
                        Riviera, che in quell'anno venne eletto (per la prima volta) come rappresentante della facolt&agrave;
                        di Scienze MM.FF.NN. Sin dall'inizio della sua esperienza da rappresentante si trov&ograve; a
                        fronteggiare un problema ben noto a molte universit&agrave; italiane: la diffidenza ed il clima
                        freddo dell'ambiente universitario:
                        <br/>Gli studenti sembravano totalmente disinteressati alle attivit&agrave; dei rappresentanti.
                        Molti di loro non sapevano nemmeno dell'esistenza di tale organo.
                        <br/>Spinto dal suo entusiasmo e dalla consapevolezza che un ambiente universitario pi&ugrave;
                        caldo e accogliente avrebbe potuto migliorare la qualit&agrave; della vita universitaria a 360
                        gradi, il neo eletto rappresentante si mise subito all'opera per cercare di abbattere il "muro
                        di ghiaccio" che divideva rappresentanti e studenti.
                        <br>
                        <br/>
                        <img class="imgLeft" src="../../assets/img/storia/2009/gallery_fsu_2009_0016.jpg" alt=""/>
                        Wudy era convinto che il problema fosse legato a due fattori primari:
                        <br/>
                        * In primo luogo le scarse opportunit&agrave; d'interazione che l'universit&agrave; lasciava:
                        essendo infatti dislocata sia dal centro d'Ateneo che dal centro della citt&agrave;, scienze ha
