                        uno STAFF. Tutto in poco pi&ugrave; di 6 settimane.
                        <br>
                        <br/>
                        Con l'intento di velocizzare la realizzazione, il progetto venne condiviso attraverso incontri
                        studenteschi negli orari extra-didattici al termine delle lezioni.
