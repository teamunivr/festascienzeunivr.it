                        di fatto, percepivano l'universit&agrave; come mero luogo di studio.
                        <br>
                        <br/>
                        Con l'obiettivo di iniziare a lavorare su entrambi i fronti, l'attenzione si focalizz&ograve;
                        sulla realizzazione di un evento...
